package main.finale.In_Walmart.nextLeap.linkedlist;


public class ListNode {

    int val;
    ListNode next;

    ListNode(int val) {
        this.val = val;
    }

    public ListNode() {
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}